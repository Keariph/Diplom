package ru.aryhlinskaya.Diplom;

public class Object {
    Integer id;
    String name;

    public Object() {
    }

    public Object(Integer id, String name) {
        this.id = id;
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
